#!/usr/bin/env node
/**
 * Prepare the workspace for benchmarking:
 * 1. Generate tailwind.config.js from the active design system's token contract
 * 2. Rebuild the design system knowledge graph
 * 3. Print "ready"
 */

import fs from 'node:fs';
import path from 'node:path';
import { execSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(__dirname, '../..');
const wsRoot = path.join(repoRoot, 'apps/workspace-react');
const activeDsCss = path.join(wsRoot, 'src/active-design-system.css');

// Step 1: Read active-design-system.css to find which DS is active
const activeCss = fs.readFileSync(activeDsCss, 'utf8');
const dsMatch = activeCss.match(/@import\s+"(.+?tokens\.css)"/);
if (!dsMatch) throw new Error('Could not find active design system');
const dsTokensPath = path.resolve(path.dirname(activeDsCss), dsMatch[1]);
const dsDir = path.dirname(dsTokensPath);
const dsId = path.basename(dsDir);

console.error(`Active design system: ${dsId}`);

// Step 2: Parse tokens.css for --color-* variables
const tokensCss = fs.readFileSync(dsTokensPath, 'utf8');
const colorRoles = new Set();
for (const m of tokensCss.matchAll(/--color-([a-z0-9-]+)\s*:/gi)) {
  colorRoles.add(m[1]);
}
const hasDark = /\[data-theme\s*=\s*"dark"\]/.test(tokensCss);
const darkSection = hasDark ? `\n  darkMode: 'class',\n` : '';

const colorEntries = [...colorRoles]
  .sort()
  .map((s) => `        '${s}': 'var(--color-${s})',`)
  .join('\n');

// Step 3: Write tailwind.config.js
const tailwindConfig = `/** GENERATED by prepare-benchmark script from ${dsId} token contract. */
/** @type {import('tailwindcss').Config} */
export default {
${darkSection}  content: [
    './src/**/*.{ts,tsx}',
    './.storybook/**/*.{ts,tsx}',
    '../../design-systems/**/code/**/*.{ts,tsx}',
  ],
  theme: {
    extend: {
      colors: {
${colorEntries}
      },
      borderRadius: { DEFAULT: 'var(--radius)' },
    },
  },
  plugins: [],
};
`;

fs.writeFileSync(path.join(wsRoot, 'tailwind.config.js'), tailwindConfig);
console.error(`tailwind.config.js generated (${colorRoles.size} color roles${hasDark ? ', dark mode' : ''})`);

// Step 4: Rebuild the graph via the CLI
const result = execSync(
  `npx tsx ${path.join(repoRoot, 'packages/cli/src/cli.ts')} graph build ${dsId}`,
  { cwd: repoRoot, encoding: 'utf8', stdio: 'pipe' }
);
console.error(`Graph rebuilt: ${result.trim()}`);

// Step 5: Return "ready"
process.stdout.write('ready\n');
