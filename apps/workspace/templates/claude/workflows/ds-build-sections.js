// ds-build-sections.js
// Build all section React components from a design system's preview HTML in parallel.
// Each section: reads preview → identifies existing vs missing primitives → crafts
// missing ones → writes section component → generates vitest test → RED/GREEN loop.
//
// Sections run via parallel() — all are independent.
//
// Usage: workflow('ds-build-sections', { dsId, dsPath, sections, maxAttempts? })
//   dsId      - kebab-case design system id
//   dsPath    - path to the design system directory
//   sections  - array of { name, selector, description?, keyComponents? }
//   maxAttempts - max RED/GREEN iterations per section (default: 4)
//
// Returns: { results: [{name, score, passed, primitivesCrafted}], primitivesCrafted: string[] }

export const meta = {
  name: 'ds-build-sections',
  description: 'Build all section React components from preview in parallel, each with vitest verification.',
  phases: [
    { title: 'Analyze sections', detail: 'Read preview HTML, identify existing vs missing primitives' },
    { title: 'Build sections', detail: 'Craft missing primitives, write section components, generate tests' },
    { title: 'Verify', detail: 'Run vitest on all section tests, RED/GREEN loop' },
  ],
}

const _args = typeof args === 'string' ? JSON.parse(args) : (args || {})
const { dsId, dsPath, sections = [], maxAttempts = 4 } = _args
if (!dsId || !dsPath) throw new Error('ds-build-sections: dsId and dsPath are required')

const previewPath = dsPath + '/reference-example.html'
const codeDir = dsPath + '/code'
const testsDir = 'src/__tests__'

log('[ds-build-sections] Building ' + sections.length + ' section(s) for "' + dsId + '"')

if (sections.length === 0) {
  log('[ds-build-sections] No sections to build')
  return { results: [], primitivesCrafted: [] }
}

// Section test template
function sectionTestTemplate(sectionName, dsPath) {
  return `// Test: Overview${sectionName} (generated by ds-build-sections)
import { describe, it, expect } from 'vitest';
import { checkPage, assertHasPageStructure, assertHasNavigation } from '@emdesign/testing';
import fs from 'node:fs';

describe('Overview${sectionName} section', () => {
  const htmlPath = '${dsPath}/reference-example.html';
  const html = fs.existsSync(htmlPath) ? fs.readFileSync(htmlPath, 'utf8') : '';

  it('has page structure', () => {
    if (html) expect(() => assertHasPageStructure(html)).not.toThrow();
  });

  it('has navigation', () => {
    if (html) expect(() => assertHasNavigation(html)).not.toThrow();
  });

  it('passes page check', () => {
    if (html) {
      const result = checkPage(html, { html, tokens: [], classes: [], textContent: '' });
      expect(result.ok).toBe(true);
    }
  });
});
`
}

// Build each section in parallel
const sectionResults = await parallel(sections.map(section => async () => {
  const sectionName = section.name
  const sectionFile = codeDir + '/Overview' + sectionName + '.tsx'
  const testFile = testsDir + '/Overview' + sectionName + '.test.ts'

  log('[ds-build-sections] Building section: ' + sectionName)

  let analysis = null
  let passed = false
  let score = 0
  let attempts = 0
  let primitivesCrafted = []

  while (attempts < maxAttempts && !passed) {
    attempts++
    log('[ds-build-sections]   [' + sectionName + '] attempt ' + attempts + '/' + maxAttempts)

    // Analyze section — read from preview, check existing primitives, identify missing
    if (!analysis || attempts > 1) {
      analysis = await agent(
        'Analyze the "' + sectionName + '" section of the preview for "' + dsId + '".\n\n' +
        'Preview: "' + previewPath + '"\nSelector: ' + (section.selector || '') + '\n' +
        'Description: ' + (section.description || '') + '\n\n' +
        'Step 1: Read the section HTML from the preview using the selector.\n' +
        'Step 2: List existing .tsx files in "' + codeDir + '":\n' +
        '  Run: ls "' + codeDir + '"/*.tsx 2>&1\n' +
        'Step 3: Identify which UI components in this section already exist vs are missing.\n' +
        '  - Existing components: import from "@ds/<Name>"\n' +
        '  - Missing components: must be crafted as new primitives\n' +
        'Step 4: Determine layout type for the section.\n\n' +
        'Return: { "existingPrimitives": string[], "missingPrimitives": [{name, htmlContext, description}], "layoutType": string, "sectionStructure": string }',
        {
          label: 'analyze:' + sectionName, phase: 'Analyze sections',
          schema: { type: 'object', properties: {
            existingPrimitives: { type: 'array', items: { type: 'string' } },
            missingPrimitives: { type: 'array', items: { type: 'object', properties: { name: { type: 'string' }, htmlContext: { type: 'string' }, description: { type: 'string' } }, required: ['name'] } },
            layoutType: { type: 'string' }, sectionStructure: { type: 'string' },
          }, required: ['existingPrimitives', 'missingPrimitives', 'layoutType'] },
        }
      )
    }

    // Craft any missing primitives
    if (analysis?.missingPrimitives?.length) {
      for (const prim of analysis.missingPrimitives) {
        try {
          await agent(
            'Create primitive "' + prim.name + '" for section "' + sectionName + '".\n\n' +
            'Preview: "' + previewPath + '"\ntokens.css: "' + dsPath + '/tokens.css"\n' +
            'Context: ' + (prim.htmlContext || prim.description || '') + '\n\n' +
            'Write to "' + codeDir + '/' + prim.name + '.tsx"\n' +
            'Requirements: Props interface, named export, semantic tokens only, className?: string.\n' +
            'Return "OK".',
            { label: 'prim:' + sectionName + ':' + prim.name, phase: 'Build sections' }
          )
          primitivesCrafted.push(prim.name)
        } catch { /* skip */ }
      }
    }

    // Write section component
    try {
      await agent(
        'Create React component Overview' + sectionName + ' for "' + dsId + '".\n\n' +
        'Write to "' + sectionFile + '"\n' +
        'Structure: ' + ((analysis && analysis.sectionStructure) || '') + '\n' +
        'Layout type: ' + ((analysis && analysis.layoutType) || 'stack') + '\n\n' +
        '1. Import existing primitives from "@ds/<Name>"\n' +
        '2. Import newly crafted primitives from "./<Name>"\n' +
        '3. Compose in the layout matching preview at "' + previewPath + '"\n' +
        '   selector: ' + (section.selector || '') + '\n' +
        '4. Use ONLY semantic token classes — NO raw hex\n' +
        '5. Export named function Overview' + sectionName + '\n' +
        'Write via Write tool. Return "OK".',
        { label: 'section:' + sectionName, phase: 'Build sections' }
      )
    } catch { /* skip */ }

    // Write vitest test file
    const testContent = sectionTestTemplate(sectionName, dsPath)
    try {
      await agent(
        'Write vitest test file for section "' + sectionName + '".\n' +
        'Steps:\n1. Run: mkdir -p ' + testsDir + '\n' +
        '2. Write to ' + testFile + ' using Write tool:\n\n' +
        '```\n' + testContent + '\n```\n\n' +
        'Return "ok".',
        { label: 'test:' + sectionName, phase: 'Verify' }
      )
    } catch { /* skip */ }

    // Run vitest
    const vitestResult = await $`npx vitest run ${testFile} --reporter=json 2>&1 || echo "EXIT_NONZERO"`
    const output = String(vitestResult || '')
    passed = !output.includes('EXIT_NONZERO') && !output.includes('failed')
    if (passed) {
      log('[ds-build-sections]   ✅ ' + sectionName + ' passed (attempt ' + attempts + ')')
    } else {
      log('[ds-build-sections]   ❌ ' + sectionName + ' failed — retrying')
    }
  }

  return { name: sectionName, passed, score, attempts, primitivesCrafted }
}))

const allCrafted = [...new Set(sectionResults.flatMap(r => r.primitivesCrafted || []))]
log('[ds-build-sections] Done: ' + sectionResults.filter(r => r.passed).length + '/' + sections.length + ' passed')
if (allCrafted.length > 0) log('[ds-build-sections] Primitives crafted: ' + allCrafted.join(', '))

return { results: sectionResults, primitivesCrafted: allCrafted }
