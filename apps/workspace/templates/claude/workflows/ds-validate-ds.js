// ds-validate-ds.js
// Validate a design system by writing a vitest test file that imports
// @emdesign/testing primitives and runs design system checks.
//
// Usage: workflow('ds-validate-ds', { dsId, dsPath })
//   dsId   - kebab-case design system id
//   dsPath - path to the design system directory
//
// Returns: { validated, findings, testFile }

export const meta = {
  name: 'ds-validate-ds',
  description: 'Validate design system via vitest test using @emdesign/testing primitives.',
  phases: [
    { title: 'Write test', detail: 'Create vitest test file with DS validation assertions' },
    { title: 'Run vitest', detail: 'Execute vitest, report findings' },
  ],
}

const _args = typeof args === 'string' ? JSON.parse(args) : (args || {})
const { dsId, dsPath } = _args
if (!dsId || !dsPath) throw new Error('ds-validate-ds: dsId and dsPath are required')

const testFile = 'src/__tests__/validate-' + dsId + '.test.ts'

phase('Write test')
log('[ds-validate-ds] Writing validation test for "' + dsId + '"')

const testContent = '// Test: ' + dsId + ' design system validation (generated by ds-validate-ds)\n' +
'import { describe, it, expect } from \'vitest\';\n' +
'import { resolveRepoPaths } from \'@emdesign/backend\';\n' +
'import { assertDesignSystemPasses, assertDesignMdComplete, checkGraph } from \'@emdesign/testing\';\n' +
'const dsId = \'' + dsId + '\';\n' +
'const paths = resolveRepoPaths();\n' +
'describe(\'Design system: \' + dsId, () => {\n' +
'  it(\'passes token contract validation\', () => {\n' +
'    const result = assertDesignSystemPasses(paths, dsId);\n' +
'    expect(result.ok).toBe(true);\n' +
'  });\n' +
'  it(\'has all 9 DESIGN.md sections\', () => {\n' +
'    expect(() => assertDesignMdComplete(paths, dsId)).not.toThrow();\n' +
'  });\n' +
'  it(\'has knowledge graph\', () => {\n' +
'    const result = checkGraph(paths, dsId);\n' +
'    expect(result.nodeCount).toBeGreaterThan(0);\n' +
'  });\n' +
'});\n'

await agent(
  'Write vitest validation test for design system "' + dsId + '".\n' +
  'Steps:\n1. Run: mkdir -p src/__tests__\n' +
  '2. Write to ' + testFile + ' using Write tool:\n\n```\n' + testContent + '\n```\n\n' +
  'Return "ok".',
  { label: 'write-val:' + dsId, phase: 'Write test' }
)
log('[ds-validate-ds] Test file: ' + testFile)

// ===== RUN VITEST =====
phase('Run vitest')
log('[ds-validate-ds] Running vitest validation')

let validated = false
const findings = []

try {
  const result = await $`npx vitest run ${testFile} --reporter=json 2>&1 || echo "EXIT_NONZERO"`
  const output = String(result || '')
  validated = !output.includes('EXIT_NONZERO') && !output.includes('failed')

  if (validated) {
    log('[ds-validate-ds] ✅ Validation passed')
  } else {
    // Extract failure messages from vitest output
    const lines = output.split('\n')
    for (const line of lines) {
      if (line.includes('AssertionError') || line.includes('×') || line.includes('FAIL')) {
        findings.push(line.trim())
      }
    }
    log('[ds-validate-ds] ❌ Validation failed: ' + findings.length + ' issue(s)')
  }
} catch (e) {
  log('[ds-validate-ds] Validation error: ' + e.message)
  findings.push('Vitest error: ' + e.message)
}

return { validated, findings, testFile }
