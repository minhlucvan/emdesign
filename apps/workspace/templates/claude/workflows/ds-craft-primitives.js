// ds-craft-primitives.js
// Craft reusable React primitive components from a design system's preview HTML,
// with vitest verification. Each component gets a .test.ts file and must pass
// vitest before it's considered complete.
//
// Usage: workflow('ds-craft-primitives', { dsId, dsPath, components, maxAttempts? })
//   dsId       - kebab-case design system id
//   dsPath     - path to the design system directory
//   components - array of { name, variants?, htmlContext? } to craft
//   maxAttempts - max RED/GREEN iterations per component (default: 3)
//
// Returns: { crafted: string[], tests: string[], results: [{name, passed, attempts}] }

export const meta = {
  name: 'ds-craft-primitives',
  description: 'Craft reusable React primitives from preview with vitest verification.',
  phases: [
    { title: 'Craft', detail: 'Write component .tsx files from preview' },
    { title: 'Test', detail: 'Generate vitest .test.ts files and run them' },
    { title: 'Fix loop', detail: 'RED/GREEN iterations until all pass' },
  ],
}

const _args = typeof args === 'string' ? JSON.parse(args) : (args || {})
const { dsId, dsPath, components = [], maxAttempts = 3 } = _args
if (!dsId || !dsPath) throw new Error('ds-craft-primitives: dsId and dsPath are required')

const codeDir = dsPath + '/code'
const testsDir = 'src/__tests__'

// Template for primitive test files (from craft-component.ts template pattern)
function primitiveTestTemplate(name, sourcePath) {
  return `// Test: ${name} primitive (generated by ds-craft-primitives)
import { describe, it, expect } from 'vitest';
import fs from 'node:fs';
import { checkLint, checkBehavior } from '@emdesign/testing';

const sourcePath = '${sourcePath}';

describe('${name} primitive', () => {
  const source = fs.existsSync(sourcePath) ? fs.readFileSync(sourcePath, 'utf8') : '';

  it('passes lint (zero P0 findings)', () => {
    const result = checkLint(source);
    expect(result.mustFix).toBe(0);
  });

  it('has proper behavior patterns', () => {
    const result = checkBehavior(source);
    expect(result.ok).toBe(true);
  });
});
`
}

log('[ds-craft-primitives] Crafting ' + components.length + ' primitive(s) for "' + dsId + '"')

if (components.length === 0) {
  log('[ds-craft-primitives] No components to craft')
  return { crafted: [], tests: [], results: [] }
}

// Craft each component in pipeline (sequential — each depends on preview context)
const results = await pipeline(
  components,
  async (comp, idx) => {
    const compName = comp.name || comp
    const compPath = codeDir + '/' + compName + '.tsx'
    const testPath = testsDir + '/' + compName + '.test.ts'

    log('[ds-craft-primitives] [' + (idx + 1) + '/' + components.length + '] ' + compName)

    let attempts = 0
    let passed = false

    while (attempts < maxAttempts && !passed) {
      attempts++
      log('[ds-craft-primitives]   Attempt ' + attempts + '/' + maxAttempts + ' for ' + compName)

      // Craft the .tsx file via agent
      try {
        await agent(
          'Create the "' + compName + '" primitive component for design system "' + dsId + '".\n\n' +
          'Context:\n' +
          '- Preview HTML: "' + dsPath + '/reference-example.html"\n' +
          '- DESIGN.md: "' + dsPath + '/DESIGN.md"\n' +
          '- tokens.css: "' + dsPath + '/tokens.css"\n' +
          '- Variants: ' + (comp.variants || 'default') + '\n' +
          '- HTML context: ' + (comp.htmlContext || '') + '\n\n' +
          'Requirements:\n' +
          '1. Read the preview HTML to understand the component structure and visual design\n' +
          '2. Use ONLY semantic token classes (bg-surface, text-accent, rounded, etc.)\n' +
          '3. NEVER use raw hex colors or hardcoded spacing\n' +
          '4. Export Props interface and named export ' + compName + '\n' +
          '5. Accept className?: string for composition\n' +
          '6. Support all variants mentioned in the description\n' +
          '7. Write to "' + compPath + '" via Write tool\n\n' +
          'Return "OK" with the component name.',
          { label: 'craft:' + compName, phase: 'Craft' }
        )
      } catch (e) {
        log('[ds-craft-primitives]   Craft error: ' + e.message)
        continue
      }

      // Write the vitest test file
      const testContent = primitiveTestTemplate(compName, compPath)
      try {
        await agent(
          'Write vitest test file for "' + compName + '".\n' +
          'Steps:\n1. Run: mkdir -p ' + testsDir + '\n' +
          '2. Write to ' + testPath + ' using Write tool:\n\n' +
          '```\n' + testContent + '\n```\n\n' +
          'Return "ok".',
          { label: 'test:' + compName, phase: 'Test' }
        )
      } catch (e) {
        log('[ds-craft-primitives]   Test file error: ' + e.message)
        continue
      }

      // Run vitest
      const vitestResult = await $`npx vitest run ${testPath} --reporter=json 2>&1 || echo "EXIT_NONZERO"`
      const output = String(vitestResult || '')
      passed = !output.includes('EXIT_NONZERO') && !output.includes('failed')
      if (passed) {
        log('[ds-craft-primitives]   ✅ ' + compName + ' passed (attempt ' + attempts + ')')
      } else {
        log('[ds-craft-primitives]   ❌ ' + compName + ' failed — will retry')
      }
    }

    return { name: compName, passed, attempts }
  }
)

const crafted = results.filter(r => r.passed).map(r => r.name)
const tests = crafted.map(name => testsDir + '/' + name + '.test.ts')
log('[ds-craft-primitives] Done: ' + crafted.length + '/' + components.length + ' passed')

return { crafted, tests, results }
